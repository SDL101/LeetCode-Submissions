class MinStack:

    def __init__(self):
        self.stack = []
        self.minStack = []

    def push(self, val: int) -> None:
        self.stack.append(val)
        if len(self.minStack) == 0:
            newMin = val
        else: 
            newMin = min(val, self.minStack[-1])
        self.minStack.append(newMin)

    def pop(self) -> None:
        self.stack.pop()
        self.minStack.pop()

    def top(self) -> int:
        return self.stack[-1]

    def getMin(self) -> int:
        return self.minStack[-1]


# Time Complexity: O(1) for all operations
# Space Complexity: O(2n) = O(n) since we have two lists that scale linearly with the input size
 
